package Casting;

public class Son extends Father {
	/*
	 * method overriding takes place.assigning super class method into subclass and
	 * changing definition according to subclass
	 */
	public void car() {
		System.out.println("Kia");

	}

	public void bike() {
		System.out.println("BMW");
	}

	public void degree() {
		System.out.println("BE");
	}

}

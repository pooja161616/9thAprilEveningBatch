package controlStudy;

public class TableOf2InForLoop {

	public static void main(String[] args) {
		// To print 2's table
		
		for(int a=2;a<=20;a=a+2)
		{
			System.out.println(a);
			
		}
		System.out.println("=================================");
		
		//table of 3
		
		for(int a=3;a<=30;a=a+3)
		{
			
			System.out.println(a);
		}

	}
}



package controlStudy;

public class DecOrderForLoop {

	public static void main(String[] args) {
		// TO write 10--->1
		
		for(int a=10;a>=1;a--)
		{
			System.out.println(a);
		}
		System.out.println("=============================");

		//Table of 5
		
		for(int a=5;a<=50;a=a+5)
		{
			System.out.println(a);
		}

	}

}

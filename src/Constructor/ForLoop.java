package Constructor;

public class ForLoop {

	public static void main(String[] args) {
		// 1,2,3,4,5
		
		for(int a=1;a<=5;a++)
		{
			System.out.print(" "+a);//For horizontal line o/p -->1 2 3 4 5
			
			//System.out.println("*");
			
		}
		System.out.println(" ");
		
		System.out.print("============================");

	

	
	for(int a=1;a<=5;a++)
	{
		System.out.println(" ");
		System.out.println("*");
		
	}


}
}


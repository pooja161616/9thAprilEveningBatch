package ImportantProgram;

public class Fibonacci_Series {

	public static void main(String[] args) {
		
		// Fibonacci series means addition of previouse 2 numbers
		//0 1 1 2 3 5 8
		
		int a=0,b=1;
		int c;
		for(int i=0;i<=10;i++) {   //statement will execute 10 times(1 to 10)
			
			c=a+b;					//a=0;b=1 and c=1
			//System.out.println(c);
			System.out.print(" "+c);
			a=b;
			b=c;//number swap for addition
			
			
			
			
			
			
			
		}

		

	}

}

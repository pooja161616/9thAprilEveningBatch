package LoopStudy;

public class forloop1 {

	public static void main(String[] args) {
		// Print 5 table in decreasing order
		
		for(int a=50;a>=5;a=a-5)
		{
			//System.out.println(a);//for verticla line
		
			System.out.print(" "+a);
		}
		System.out.println();
		System.out.println("================================");
		
		//print 1 to 10 numbers
		
		for(int a=1;a<=10;a++)
		{
			System.out.println(a);
		}

	}

}

package LoopStudy;

public class While_Loop {

	public static void main(String[] args) {
		       // 5 table using while Loop
		
				//initialization:int a=5;
				//Condition : a<=50;
				//Updation : a=a+5;
		
		int a=5;
		
		while (a<=50) 
		{
			System.out.println(a);
			a=a+5;
		}
		
		System.out.println("=====================================");
		
		// 5 table decreasing order using while Loop
		
         int x=50;
         while (x>=5)
         {
			System.out.println(x);
			x=x-5;
		 }
	}

}

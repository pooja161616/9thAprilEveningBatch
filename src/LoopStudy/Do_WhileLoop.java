package LoopStudy;

public class Do_WhileLoop {

	public static void main(String[] args) {
		// 5 table using Do while Loop
		
		int a=5;
		
		do 
		{
			
			System.out.println(a);
			a=a+5;
		}
		
		while(a<=50);
		
		System.out.println("=========================");
		
		//5 table in decreasing order
		
		int x=50;
		do
		{
			System.out.println(x);
			x=x-5;
		}
		while(x>=5);
		}
	
	
}
		